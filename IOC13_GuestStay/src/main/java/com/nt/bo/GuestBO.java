package com.nt.bo;

public class GuestBO {
	private String name;
	private String addr;
	private String food_ordered;
	private Integer no_of_days;
	private Float total_amt;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getFood_ordered() {
		return food_ordered;
	}
	public void setFood_ordered(String food_ordered) {
		this.food_ordered = food_ordered;
	}
	public Integer getNo_of_days() {
		return no_of_days;
	}
	public void setNo_of_days(Integer no_of_days) {
		this.no_of_days = no_of_days;
	}
	public Float getTotal_amt() {
		return total_amt;
	}
	public void setTotal_amt(Float total_amt) {
		this.total_amt = total_amt;
	}
	
	
}
